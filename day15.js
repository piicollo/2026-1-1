// switch statement 

let gender = 4;

switch (gender) {
    case 1:
        console.log(`Gender: Male`);
        break;
    case 2:
        console.log(`Gender: Female`);
        break;
    default:
        console.log(`Gender: Others`);
}

let snacks = 'burger';

switch (snacks) {
    case 'momo':
        console.log(`Nepali dumplings`); 
        break;
    case 'Thakali':
        console.log(`Nepali cuisine`);
        
    default:
        console.log(`Out of Order`);
        
}

let book = 'computer science';

switch (book) {
    case 'computer science' :
        console.log('Book about computers');
        break;
    case 'Nepali' :
        console.log('Book about Nepali language');
        break;
    case 'math':
        console.log('Book about calculation');
        break;
    default:
        console.log('Out of context');
                    
        
}